# Creacion de la Clase Trig

import math

class Trig:

    def __init__(self):#esta es la clase para definiar la clase
        
        self.pi = math.pi
        

    def sen(self): #Metodo para el seno de pi
        return math.sin(self.pi)

    def cos(self):
        return math.cos(self.pi) #Metodo para el coseno de pi
    
    def tan(self):
        return math.tan(self.pi) #Metodo para la tangente de pi
    
